public class BitManipulation{
    public static void main(String args[]){
        // AND operator
        System.out.println((5 & 6));
        // OR operator
        System.out.println((5 | 6 ));
        // XOR operator
        System.out.println((5 ^ 6));
        // Binary one's complement
        System.out.println((~5));
        System.out.println((~0));
        // Binary left shift
        System.out.println((5 << 2));
        // Binary right shift
        System.out.println((5 >> 2));
        System.out.println((6 >> 1));
    }
}