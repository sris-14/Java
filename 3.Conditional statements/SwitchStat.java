public class SwitchStat {
    // Switch cases :
    /*
     switch(var){
        case 1:
        case 2:
        case 3:
        default:
     }
    */ 
    public static void main(String args[]){
          int num=2;
          switch(num){
            case 1: System.out.println("juice");
                    break;
            case 2: System.out.println("mango shake");
                    break;
            case 3: System.out.println("chocobar");
                    break;
            default: System.out.println("we are dreaming");
          }
    }
}
